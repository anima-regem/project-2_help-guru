const subtitleBlocks = subtitleFile.trim().split("\n");

// Parse each subtitle block into an object with a `startTime`, `endTime`, and `text` property
const subtitles = subtitleBlocks.map((block) => {
  const [idLine, timeLine, ...textLines] = block.split("\n");
  const [startTime, endTime] = timeLine
    .split(" --> ")
    .map((time) => new Date(`1970-01-01T${time}Z`).getTime());
  const text = textLines.join("\n");
  return { startTime, endTime, text };
});

// Sort the subtitles by their `startTime` property
subtitles.sort((a, b) => a.startTime - b.startTime);
export default subtitles;
